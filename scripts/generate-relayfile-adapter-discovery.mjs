#!/usr/bin/env node
import { existsSync } from "node:fs";
import { readdir, readFile, writeFile } from "node:fs/promises";
import { join, relative } from "node:path";
import { fileURLToPath } from "node:url";

const repoRoot = fileURLToPath(new URL("..", import.meta.url));
const outputPath = join(
  repoRoot,
  "packages/core/src/sync/adapter-discovery-authored.generated.ts",
);
const check = process.argv.includes("--check");

async function readJson(path) {
  return JSON.parse(await readFile(path, "utf8"));
}

async function directAdapterPackages() {
  const manifests = await Promise.all([
    readJson(join(repoRoot, "package.json")),
    readJson(join(repoRoot, "packages/core/package.json")),
  ]);
  const names = new Set();
  for (const manifest of manifests) {
    for (const section of ["dependencies", "devDependencies"]) {
      for (const name of Object.keys(manifest[section] ?? {})) {
        if (name.startsWith("@relayfile/adapter-") && name !== "@relayfile/adapter-core") {
          names.add(name);
        }
      }
    }
  }
  return [...names].sort((a, b) => a.localeCompare(b));
}

async function walkFiles(dir) {
  const entries = await readdir(dir, { withFileTypes: true });
  const files = [];
  for (const entry of entries.sort((a, b) => a.name.localeCompare(b.name))) {
    const fullPath = join(dir, entry.name);
    if (entry.isDirectory()) {
      files.push(...await walkFiles(fullPath));
    } else if (entry.isFile()) {
      files.push(fullPath);
    }
  }
  return files;
}

async function buildArtifacts() {
  const artifacts = {};
  const sources = [];
  for (const packageName of await directAdapterPackages()) {
    const packageRoot = join(repoRoot, "node_modules", ...packageName.split("/"));
    const discoveryRoot = join(packageRoot, "discovery");
    if (!existsSync(discoveryRoot)) continue;

    sources.push(packageName);
    for (const file of await walkFiles(discoveryRoot)) {
      const key = relative(packageRoot, file).split("\\").join("/");
      artifacts[key] = await readFile(file, "utf8");
    }
  }
  return { artifacts, sources };
}

function render({ artifacts, sources }) {
  return `// Generated by scripts/generate-relayfile-adapter-discovery.mjs.
// Source of truth: package-owned discovery/ artifacts from these direct deps:
${sources.map((source) => `// - ${source}`).join("\n")}
// Do not edit by hand. Run:
//   npm run relayfile-adapter-discovery:generate

export const AUTHORED_ADAPTER_DISCOVERY_ARTIFACTS: Record<string, string> = ${JSON.stringify(artifacts, null, 2)};
`;
}

const next = render(await buildArtifacts());
if (check) {
  const current = existsSync(outputPath) ? await readFile(outputPath, "utf8") : "";
  if (current !== next) {
    console.error(
      "adapter-discovery-authored.generated.ts is out of date. Run `npm run relayfile-adapter-discovery:generate`.",
    );
    process.exit(1);
  }
} else {
  await writeFile(outputPath, next);
}
