#!/usr/bin/env node
import { readFileSync, writeFileSync } from "node:fs";
import { resolve } from "node:path";
import { fileURLToPath } from "node:url";

const OUTPUT_PATH = "nango-integrations/linear-relay/shared/query-fragments.generated.ts";

const args = new Set(process.argv.slice(2));
const check = args.has("--check");
const sourceArg = process.argv.find((arg) => arg.startsWith("--source="));
const sourcePath = sourceArg ? sourceArg.slice("--source=".length) : process.env.LINEAR_ADAPTER_TYPES_SOURCE;

const labelFields = sourcePath
  ? readLinearLabelFieldsFromSource(resolve(process.cwd(), sourcePath))
  : readLinearLabelFieldsFromInstalledPackage();
const rendered = render(labelFields);
const outputPath = resolve(process.cwd(), OUTPUT_PATH);

if (check) {
  const current = readFileSync(outputPath, "utf8");
  if (current !== rendered) {
    console.error(`${OUTPUT_PATH} is out of date with @relayfile/adapter-linear LinearLabel.`);
    console.error("Run: node scripts/generate-nango-linear-query-fragments.mjs");
    process.exit(1);
  }
  console.log(`${OUTPUT_PATH} is in sync.`);
} else {
  writeFileSync(outputPath, rendered);
  console.log(`Generated ${OUTPUT_PATH} from @relayfile/adapter-linear LinearLabel.`);
}

function readLinearLabelFieldsFromInstalledPackage() {
  const packageTypesUrl = import.meta.resolve("@relayfile/adapter-linear/types");
  const typesPath = fileURLToPath(packageTypesUrl).replace(/\.js$/u, ".d.ts");
  return readLinearLabelFieldsFromSource(typesPath);
}

function readLinearLabelFieldsFromSource(path) {
  const source = readFileSync(path, "utf8");
  const match = source.match(/export interface LinearLabel \{([\s\S]*?)\n\}/u);
  if (!match?.[1]) {
    throw new Error(`Could not find LinearLabel interface in ${path}`);
  }

  const body = match[1];
  const fields = new Set([...body.matchAll(/^\s+([A-Za-z][A-Za-z0-9_]*)\??:/gmu)].map((entry) => entry[1]));
  const requiredFields = ["id", "name", "description", "color", "team", "parent", "createdAt", "updatedAt"];
  const missing = requiredFields.filter((field) => !fields.has(field));
  if (missing.length > 0) {
    throw new Error(`LinearLabel in ${path} is missing expected fields: ${missing.join(", ")}`);
  }

  return requiredFields;
}

function render(labelFields) {
  const sourceFields = labelFields.join(", ");
  return `// Generated by scripts/generate-nango-linear-query-fragments.mjs from
// @relayfile/adapter-linear/types LinearLabel (${sourceFields}). Do not edit by hand.
//
// Nango compile disallows non-allowlisted package imports, including
// @relayfile/adapter-linear. This generated relative module lets Linear syncs
// consume adapter-owned label shape without hand-maintained field fragments.
// archivedAt is a Linear lifecycle field required so archived labels stay
// materialized instead of leaving stale active records behind.
export const LINEAR_LABEL_FIELDS = \`
        id
        name
        description
        color
        archivedAt
        createdAt
        updatedAt
        team {
          id
          key
          name
        }
        parent {
          id
          name
        }
\`;
`;
}
