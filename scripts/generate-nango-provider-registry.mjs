import { readFile, writeFile } from "node:fs/promises";
import path from "node:path";

const repoRoot = process.cwd();
const nangoConfigPath = path.join(repoRoot, "nango-integrations/.nango/nango.json");
const schemaPath = path.join(repoRoot, "nango-integrations/.nango/schema.json");
const outputPath = path.join(
  repoRoot,
  "packages/core/src/sync/nango-provider-registry.generated.ts",
);

function assertString(value, label) {
  if (typeof value !== "string" || value.length === 0) {
    throw new Error(`Invalid nango registry: ${label} must be a non-empty string`);
  }
  return value;
}

const nangoConfig = JSON.parse(await readFile(nangoConfigPath, "utf8"));
const schema = JSON.parse(await readFile(schemaPath, "utf8"));

if (!Array.isArray(nangoConfig)) {
  throw new Error("Invalid nango registry: .nango/nango.json must be an array");
}

const definitions = schema?.definitions;
if (!definitions || typeof definitions !== "object") {
  throw new Error("Invalid nango registry: .nango/schema.json must expose definitions");
}

const entries = [];
const missingModels = [];

for (const integration of nangoConfig) {
  const provider = assertString(integration.providerConfigKey, "providerConfigKey");
  for (const sync of integration.syncs ?? []) {
    const syncName = assertString(sync.name, `${provider}.sync.name`);
    for (const model of sync.output ?? []) {
      const modelName = assertString(model, `${provider}.${syncName}.output`);
      const syncLocalDefinitions = sync?.json_schema?.definitions;
      const hasModelDefinition =
        Boolean(definitions[modelName]) ||
        Boolean(syncLocalDefinitions && typeof syncLocalDefinitions === "object" && syncLocalDefinitions[modelName]);
      if (!hasModelDefinition) {
        missingModels.push(`${provider}:${syncName}:${modelName}`);
      }
      entries.push({
        key: `${provider}:${syncName}:${modelName}`,
        provider,
        sync: syncName,
        model: modelName,
      });
    }
  }
}

if (missingModels.length > 0) {
  throw new Error(
    `Invalid nango registry: sync output models missing from schema definitions:\n${missingModels.join("\n")}`,
  );
}

const seen = new Set();
const duplicates = [];
for (const entry of entries) {
  if (seen.has(entry.key)) {
    duplicates.push(entry.key);
  }
  seen.add(entry.key);
}

if (duplicates.length > 0) {
  throw new Error(
    `Invalid nango registry: duplicate provider/sync/model keys:\n${duplicates.join("\n")}`,
  );
}

const contents = `// Generated by scripts/generate-nango-provider-registry.mjs.
// Do not edit by hand. The source of truth is nango-integrations/.nango/nango.json.

export const GENERATED_NANGO_PROVIDER_MODEL_REGISTRY = ${JSON.stringify(entries, null, 2)} as const;

export type GeneratedNangoProviderModelEntry =
  (typeof GENERATED_NANGO_PROVIDER_MODEL_REGISTRY)[number];

export type GeneratedNangoProviderModel =
  GeneratedNangoProviderModelEntry["key"];
`;

await writeFile(outputPath, `${contents}\n`);
