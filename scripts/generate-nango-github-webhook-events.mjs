#!/usr/bin/env node
import { readFileSync, writeFileSync } from "node:fs";
import { resolve } from "node:path";
import { pathToFileURL } from "node:url";

const OUTPUT_PATH = "nango-integrations/github-relay/shared/webhook-events.ts";

const args = new Set(process.argv.slice(2));
const check = args.has("--check");
const sourceArg = process.argv.find((arg) => arg.startsWith("--source="));
const sourcePath = sourceArg ? sourceArg.slice("--source=".length) : process.env.GITHUB_ADAPTER_TYPES_SOURCE;

const events = sourcePath
  ? readEventsFromTypesSource(sourcePath)
  : await readEventsFromInstalledPackage();
const rendered = render(events);
const outputPath = resolve(process.cwd(), OUTPUT_PATH);

if (check) {
  const current = readFileSync(outputPath, "utf8");
  if (current !== rendered) {
    console.error(`${OUTPUT_PATH} is out of date with @relayfile/adapter-github DEFAULT_GITHUB_EVENTS.`);
    console.error("Run: node scripts/generate-nango-github-webhook-events.mjs");
    process.exit(1);
  }
  console.log(`${OUTPUT_PATH} is in sync.`);
} else {
  writeFileSync(outputPath, rendered);
  console.log(`Generated ${OUTPUT_PATH} from @relayfile/adapter-github DEFAULT_GITHUB_EVENTS.`);
}

async function readEventsFromInstalledPackage() {
  const packageEntryUrl = import.meta.resolve("@relayfile/adapter-github");
  const mod = await import(new URL("./types.js", packageEntryUrl).href);
  if (!Array.isArray(mod.DEFAULT_GITHUB_EVENTS)) {
    throw new Error("@relayfile/adapter-github does not export DEFAULT_GITHUB_EVENTS");
  }
  return assertStringArray(mod.DEFAULT_GITHUB_EVENTS, "@relayfile/adapter-github DEFAULT_GITHUB_EVENTS");
}

function readEventsFromTypesSource(path) {
  const source = readFileSync(resolve(process.cwd(), path), "utf8");
  const match = source.match(/export const DEFAULT_GITHUB_EVENTS = \[([\s\S]*?)\] as const;/);
  if (!match?.[1]) {
    throw new Error(`Could not find DEFAULT_GITHUB_EVENTS in ${path}`);
  }
  const moduleUrl = pathToFileURL(resolve(process.cwd(), path)).href;
  const events = [...match[1].matchAll(/'([^']+)'/g)].map((entry) => entry[1]);
  return assertStringArray(events, `${moduleUrl} DEFAULT_GITHUB_EVENTS`);
}

function assertStringArray(values, label) {
  if (values.length === 0 || values.some((value) => typeof value !== "string" || value.length === 0)) {
    throw new Error(`${label} must be a non-empty string array`);
  }
  return [...values];
}

function render(events) {
  return `// Generated by scripts/generate-nango-github-webhook-events.mjs from
// @relayfile/adapter-github DEFAULT_GITHUB_EVENTS. Do not edit by hand.
//
// Nango compile disallows non-allowlisted package imports, including
// @relayfile/adapter-github. This generated relative module lets Nango compile
// consume adapter-owned declarations without hand-maintained duplicates.
const RELAYFILE_GITHUB_EVENTS = [
${events.map((event) => `    '${event}',`).join("\n")}
] as const;

export const GITHUB_ISSUE_SYNC_WEBHOOK_SUBSCRIPTIONS = githubEventsFor('issues');
export const GITHUB_PULL_REQUEST_SYNC_WEBHOOK_SUBSCRIPTIONS = githubEventsFor('pull_request');
// Check events power the failing-CI auto-fix persona (cloud#1438 follow-on S5):
// a check_run.completed with conclusion=failure must reach integration-watch.
// Both groups are adapter-sourced — they only contribute event names the
// adapter's DEFAULT_GITHUB_EVENTS actually declares, so check_suite is a no-op
// until the adapter ships it (zero hand-maintained drift).
export const GITHUB_CHECK_RUN_SYNC_WEBHOOK_SUBSCRIPTIONS = githubEventsFor('check_run');
export const GITHUB_CHECK_SUITE_SYNC_WEBHOOK_SUBSCRIPTIONS = githubEventsFor('check_suite');
// Deployment-status events power deploy-watch agents (NightCTO): a
// deployment_status.created carries deploy outcome in deployment_status.state
// (success / failure / error). Adapter-sourced — contributes only when the
// adapter's DEFAULT_GITHUB_EVENTS declares deployment_status.* (zero drift).
export const GITHUB_DEPLOYMENT_STATUS_SYNC_WEBHOOK_SUBSCRIPTIONS = githubEventsFor('deployment_status');
export const GITHUB_SYNC_WEBHOOK_EVENTS = githubWebhookEventNames([
    ...GITHUB_ISSUE_SYNC_WEBHOOK_SUBSCRIPTIONS,
    ...GITHUB_PULL_REQUEST_SYNC_WEBHOOK_SUBSCRIPTIONS,
    ...GITHUB_CHECK_RUN_SYNC_WEBHOOK_SUBSCRIPTIONS,
    ...GITHUB_CHECK_SUITE_SYNC_WEBHOOK_SUBSCRIPTIONS,
    ...GITHUB_DEPLOYMENT_STATUS_SYNC_WEBHOOK_SUBSCRIPTIONS
]);
export const DEFAULT_RELAYFILE_GITHUB_WEBHOOK_EVENTS = githubWebhookEventNames(RELAYFILE_GITHUB_EVENTS);

function githubEventsFor(prefix: string): string[] {
    return RELAYFILE_GITHUB_EVENTS.filter((event) => event.startsWith(\`\${prefix}.\`));
}

function githubWebhookEventNames(events: readonly string[]): string[] {
    return uniqueStrings(events.map((event) => event.split('.', 1)[0] ?? event));
}

function uniqueStrings(values: readonly string[]): string[] {
    return [...new Set(values)];
}
`;
}
